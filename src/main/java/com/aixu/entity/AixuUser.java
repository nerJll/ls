package com.aixu.entity;

/**
 * @desc 当前登陆人信息
 * @date 2017年12月20日
 */
public class AixuUser implements java.io.Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 5943728052229639245L;
	
	private String userId;
	private String company;
	private String depId;
	private String position;
	private String phoneNo;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getDepId() {
		return depId;
	}

	public void setDepId(String depId) {
		this.depId = depId;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

	@Override
	public String toString() {
		return "AixuUser [userId=" + userId + ", company=" + company + ", depId=" + depId + ", position=" + position
				+ ", phoneNo=" + phoneNo + "]";
	}

}
